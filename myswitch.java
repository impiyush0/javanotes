import java.util.Scanner;

public class myswitch {
    public static void main(String[] args) {
        int number = 3;
        switch (number) {
            case 1:
                System.out.println("ferrari");
                break;
            case 2:
                System.out.println("money");
                break;
            case 3:
                System.out.println("girl");
                break;
            default:
                System.out.println("wake up");
        }
    }
}
